-- 将该方向视为有 5 米的自由空间

-- ========================================
-- IMU 配置
-- ========================================
TRAJECTORY_BUILDER_2D.use_imu_data = true
-- 是否使用 IMU 数据
-- true = 使用 IMU 提供重力方向和角速度
-- 可以提高建图精度，特别是在快速旋转时
-- 如果 IMU 数据不稳定，设为 false

-- ========================================
-- 扫描匹配配置
-- ========================================
TRAJECTORY_BUILDER_2D.use_online_correlative_scan_matching = true
-- 使用在线相关性扫描匹配
-- true = 在搜索窗口内穷举搜索最佳匹配位置
-- 比纯 Ceres 优化更稳定、更鲁棒
-- 但计算量更大

return options
-- 返回配置选项
-- Cartographer 会加载这个配置
