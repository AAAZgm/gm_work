include "trajectory_builder.lua" 
-- 引入轨迹构建器默认配置
-- 提供局部建图、扫描匹配、位姿估计等功能

-- ========================================
-- 主配置选项
-- ========================================
options = {
  -- ========================================
  -- 构建器实例
  -- ========================================
  map_builder = MAP_BUILDER,
  -- 使用默认地图构建器实例
  -- 负责：submaps 管理、回环检测、全局位姿优化
  
  trajectory_builder = TRAJECTORY_BUILDER,
  -- 使用默认轨迹构建器实例
  -- 负责：单条轨迹的扫描匹配、局部建图、位姿跟踪

  -- ========================================
  -- 坐标系配置
  -- ========================================
  map_frame = "map",

  tracking_frame = "imu_link",
  -->传感器到这来
  published_frame = "odom",
  --map->

  odom_frame = "odom",

  -- ========================================
  -- TF 发布配置
  -- ========================================
  provide_odom_frame = false,

  publish_frame_projected_to_2d = true,
  -- 将 3D 姿态投影到 2D 平面
  -- true = 强制 2D 平面运动（地面机器人）
  -- 忽略 Z 轴位移和 roll/pitch 角
  -- 只保留 x, y, yaw
  
  use_pose_extrapolator = true,
  -- 是否使用位姿外推器
  -- true = 平滑位姿输出，预测未来时刻的位置
  -- 可以在传感器数据延迟时提供平滑的 TF

  -- ========================================
  -- 传感器使用配置
  -- ========================================
  use_odometry = true,
  -- 是否使用外部里程计数据
  -- true = 订阅 /odom 话题
  -- 里程计提供短期运动约束，提高定位稳定性
  
  use_nav_sat = false,
  -- 是否使用 GPS 数据
  -- false = 室内环境，不使用 GPS
  -- true = 订阅 /nav_sat 话题
  
  use_landmarks = false,
  -- 是否使用路标数据
  -- false = 没有路标检测系统
  -- true = 订阅 /landmark 话题

  -- ========================================
  -- 激光雷达配置
  -- ========================================
  num_laser_scans = 1,
  -- 单线激光雷达数量
  -- 1 = 使用 1 个单线激光雷达
  -- Cartographer 会订阅 /scan 话题
  
  num_multi_echo_laser_scans = 0,
  -- 多回波激光雷达数量
  -- 0 = 不使用多回波激光（普通激光雷达）
  
  num_subdivisions_per_laser_scan = 1,
  -- 每次扫描分割成多少段处理
  -- 1 = 不分割，整帧作为一个处理单元
  -- 大于 1 = 用于高速运动时的运动畸变校正
  
  num_point_clouds = 0,
  -- 3D 点云传感器数量
  -- 0 = 2D 建图模式，不使用 3D 点云

  -- ========================================
  -- 时间参数
  -- ========================================
  lookup_transform_timeout_sec = 0.2,
  -- TF 查询超时时间（秒）
  -- 查询 TF 时等待的最长时间
  
  submap_publish_period_sec = 0.5,
  -- 子地图发布周期（秒）
  -- 多长时间发布一次子地图数据
  
  pose_publish_period_sec = 5e-3,
  -- 位姿发布周期（秒）
  -- 5e-3 = 0.005 秒 = 200Hz
  -- 高频率发布位姿，确保 TF 平滑
  
  trajectory_publish_period_sec = 30e-3,
